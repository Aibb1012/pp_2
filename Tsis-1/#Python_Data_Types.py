#Python Data Types

x = 5
print(type(x))  #int datatype

x = "Hello World"
print(type(x)) #str datatype

x = 20.5
print(type(x)) #float datatype

x = ["apple", "banana" , "cherry"]
print(type(x)) #list datatype

x = ("apple", "banana" , "cherry")
print(type(x)) #tuple

x = {"name": "John" , "age":36}
print(type(x)) #dict

x = True
print(type(x)) #bool