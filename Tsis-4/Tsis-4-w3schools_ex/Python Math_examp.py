# Python Math_examp
# The min() and max() functions can be used to find the lowest or highest value in an iterable:
x = min(5, 10, 25)
y = max(5, 10, 25)
print(x)
print(y)
#the abs() function
x = abs(-7.25)
print(x)

# The pow(x, y) function returns the value of x to the power of y (xy).
x = pow(4, 3)
print(x)

#math module
import math
x = math.sqrt(64)
print(x)
#ceil and floor
import math
x = math.ceil(1.4)
y = math.floor(1.4)
print(x) # returns 2
print(y) # returns 1

